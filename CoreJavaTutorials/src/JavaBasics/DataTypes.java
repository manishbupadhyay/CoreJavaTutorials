package JavaBasics;

public class DataTypes {

	// main() method --> starting/execution point of the program
	public static void main(String[] args) {
		// primtive data types : int, double, char, boolean
		// 1- int
		int i = 7;
		int j = 20;
		System.out.println(j);
		int k = 30;
		System.out.println(i+j+k);
		
		// 2- double
		double d = 12.79;
		double d1 = 32.45;
		double d2 = 100; //100.0
		
		// 3 - Char: only single digit value allow and should be written in single quote 
		char c = 'a';
		char c1 = 'A';
		char c2 = '1';
		char c3 = '$';
		System.out.println(c3);
		
		// 4 - boolean
		boolean b1 = true;
		boolean b2 = false;
		
		// 5- String: is a class not a datatype but we can use as a datatype;
		String s = "Hello World";
		String s1 = "Selenium";
		String s2 = "Hi this is my java code";
		String s3 = "1000";
		String s4 = "10.44";
		
		
		
		System.out.println(d2);

	}

}
